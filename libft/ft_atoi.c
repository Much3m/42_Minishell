#include "libft.h"

int	ft_atoi(const char *str)
{
	int	neg;
	int	result;
	int	i;

	neg = 1;
	result = 0;
	i = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
		|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		result = 10 * result + (int)(str[i++] - '0');
	return (neg * result);
}
